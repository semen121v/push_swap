#include "libft.h"

size_t		ft_wordcount(const char *s, char c)
{
	size_t	i;
	size_t	w;

	i = 0;
	w = 0;
	while (s[i] != '\0')
	{
		if (s[i] != c)
			w++;
		while (s[i] != c && s[i + 1] != '\0')
			i++;
		i++;
	}
	return (w);
}
