#include "libft.h"

int		ft_putstr(char const *s)
{
	int		i;
	char	*str;

	str = (char*)s;
	i = 0;
	while (s != NULL && str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
	return (0);
}
