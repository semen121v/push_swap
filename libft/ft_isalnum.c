/*
** Checking ch is digit or alpha.
*/

int		ft_isalnum(int ch)
{
	if (ch >= 48 && ch <= 57)
		return (1);
	if (ch >= 65 && ch <= 90)
		return (1);
	if (ch >= 97 && ch <= 122)
		return (1);
	return (0);
}
